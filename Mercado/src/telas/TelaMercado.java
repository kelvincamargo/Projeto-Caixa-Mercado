    private void txtcodigoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcodigoKeyReleased
       
        if (txtcodigo.getText().length() == 13)
        {
               evt.setKeyCode(KeyEvent.VK_ENTER);
       
        
        if(evt.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER) 
        {
        pegaFoto();
        CarregaDados();
        carregaBarCode();
        txtcodigo.setText("");
        tota=tota+(Tatal);
        txttotal.setText(cm.ConvMoeda(String.valueOf(tota)));
       
        
        }
        
        }
         
        if(evt.getKeyCode() == java.awt.event.KeyEvent.VK_ESCAPE) 
        {  
            JOptionPane sair = new JOptionPane();
            
            int Sair = sair.showConfirmDialog(null,"CONFIRME PARA ENCERRAR O CAIXA","Sair",JOptionPane.YES_NO_OPTION);
            
            if(Sair == JOptionPane.YES_OPTION)
            {
                 System.exit(0);           
            }  
        }
        
        if(evt.getKeyCode() == java.awt.event.KeyEvent.VK_F5) 
        {  
                 txtcodigo.setEnabled(false);
                 txtqtde.setEditable(true);
                 txtqtde.setText("");
                 txtqtde.requestFocus();
                 
                 
                 }  
               
        if(evt.getKeyCode() == java.awt.event.KeyEvent.VK_F1) 
        {  
            if  (txtrecebido.getText().equals ("R$ 0,00"))
            
                 txtcodigo.setEnabled(false);
            txtrecebido.setEnabled(true);
                  txtrecebido.setText("");
                 txtrecebido.requestFocus(); 
        }  
                       
               
    }//GEN-LAST:event_txtcodigoKeyReleased

